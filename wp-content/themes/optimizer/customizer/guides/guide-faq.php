        <li>
        	<p><b>Why am I not seeing the Slider and the widgets I added to my frontpage?</b></p>
        	<p> It's because your frontpage is set to a Static Page. Please go to Settings > Reading and set your frontpage to "Your Latest Posts".</p>
        </li>
    
        <li>
        	<p><b>What is the Optimum Size for Slider Images?</b></p>
        	<p> The Optimum Slider image width should be at-least 1920px and the height should vary depending on your slider content. If you have good amount of slider content, make sure the image is taller than usual to fit the content.</p>
        </li>
    
    
        <li>
        	<p><b>How do I link widgets to my Slider CTA buttons or my menu to make a jump to that widget?</b></p>
        	<p> You can only do that with Optimizer PRO. After activating Optimizer PRO, follow <a target="_blank" href="https://optimizerwp.com/documentation/make-menu-linked-to-frontpage-elements/">this instruction</a>. </p>
        </li>
    
        <li>
        	<p><b>Why is my slider image is not displaying properly on Mobile devices?</b></p>
        	<p> Optimizer has a smart resize feature to that resize the slider image on mobile to fit the content inside the slider. You can disable it by going to Appearance > Customize > Miscellaneous > Mobile Layout and disabling the "Smart Resize Slider Image" option.</p>
        </li>
    
    
    </ul>
    
