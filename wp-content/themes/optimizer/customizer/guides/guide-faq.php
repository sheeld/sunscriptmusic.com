    <ul>
    
        <li>
        	<p><b>Why am I not seeing the Slider and the widgets I added to my frontpage?</b></p>
        	<p> It's because your frontpage is set to a Static Page. Please go to Settings > Reading and set your frontpage to "Your Latest Posts".</p>
        </li>
    
    
        <li>
        	<p><b>How do I change the blue color that is seen throughout the site?</b></p>
        	<p> You can change the blue color by going to Appearance > Customize > Basic > Basic Style and changing the "Other Elements Background" option.</p>
        </li>
        
    
        <li>
        	<p><b>How do I center images & Text inside Widgets?</b></p>
        	<p> You can center your images or text by selecting them and clicking the "Align Center" button from the editor toolbar.</p>
        </li>
    
        <li>
        	<p><b>How do I link my image to a Custom Link?</b></p>
        	<p> You can link your image to an external/custom link while inserting the image or editing the image. Just notice on the right panel of the insert media screen there is an option that says "Link To". Select "Custom Url" and insert you custom url in the url field.</p>
        </li>
    
        <li>
        	<p><b>How do I add Favicon to my site?</b></p>
        	<p> WordPress has a built-in favicon feature. Just Go to Appearance > Customize > Basic > Site Identity and upload your image.</p>
        </li>
    

    </ul>
    
    
