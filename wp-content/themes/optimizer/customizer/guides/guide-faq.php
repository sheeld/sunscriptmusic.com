            <p>When creating other pages with widgets, the Page header is not shown by default; You can override this behavior by going to Cusotmize > Post & Page > Page Header Settings and enable the "Enable Page Header in Widgetized Pages" option.</p>
        </li>
        
        <li>
            <p><b>How do I Enable/Disable Comments on my site?</b></p>
            <p>Go to Appearance > Customize > Post & Page > Posts Settings and switch the "Comments" option to enable/disable the comments system. You can also, enable/disable comments by going to Settings > Discussion and switching the "Allow people to post comments on new articles".</p>
