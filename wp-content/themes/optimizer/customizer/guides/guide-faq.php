<div class="customizer_tab faq_tabs">

    <ul>
    
        <li>
        	<p><b>When I click the 'Edit Content' button the editor shows up but I cannot insert any content inside the editor. How do I fix this?</b></p>
        	<p> This may happen for few reasons:</p>
                <ol>
                    <li>When there is a plugin that tries to modify the editor. Please disable all the plugins and then enable them one by one and find out which plugin is causing this. Report the plugin that broke the Editor in the Support Forum.</li>
                    <li>2. When you have disabled Visual Editor from your profile settings. From your WordPress Dashboard Go to Users > Your Profile and make sure the "Disable the visual editor when writing" is unchecked.</li>
                </ol>
        	
        </li>
    
        <li>
        	<p><b>When I go to Appearance > Customize, I am getting a "500 Internal Server error message" How do I fix this?</b></p>
        	<p> It's happening because your WordPress has a limited memory. You can increase the memory by installing the "Change Memory Limit" plugin and Then go to Tools > Memory Limit and increase the memory to 256 mb.</p>
        </li>
    
        <li>
        	<p><b>I am getting a "Cheatin' uh?" message inside the Customizer, why?</b></p>
        	<p> This is default wordpress security message. It's displayed when the session inside customizer is timed out. When you keep your customizer open for a long time without any activity and then try to make changes, you will get this message because your session was disconnected. Try refreshing the page and you won't get the message.</p>
        </li>
    
        <li>
        	<p><b>How do I place widgets side by side on my Frontpage?</b></p>
        	<p> You can do that with PRO version only.</p>
        </li>
    
    
    
    </ul>

